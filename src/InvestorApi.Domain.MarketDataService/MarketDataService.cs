        public IReadOnlyCollection<ShareInfo> FindShares(string searchTerm)
        {
            return Shares
                .Where(share => share.Key.IndexOf(searchTerm, StringComparison.OrdinalIgnoreCase) > -1 || share.Value[0].IndexOf(searchTerm, StringComparison.OrdinalIgnoreCase) > -1)
                .Select(share => new ShareInfo(share.Key, share.Value[0]))
                .ToList();
        }

        public IReadOnlyCollection<ShareDetails> GetShareDetails(IEnumerable<string> symbols)
        {
            return symbols
                .Select(symbol => new ShareDetails(symbol, Shares[symbol][0], 100.00m))
                .ToList();
        }
